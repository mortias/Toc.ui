package com.mitc.dto;

public class SettingsForm {

    private int width;
    private int height;

    public SettingsForm() {
    }

    public int getWidth() {
        return width;
    }

    public void setWidth(int width) {
        this.width = width;
    }

    public int getHeight() {
        return height;
    }

    public void setHeight(int height) {
        this.height = height;
    }

}
